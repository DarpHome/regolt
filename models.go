type Setting struct {
	Timestamp int64
	Value     string
}

func (s *Setting) UnmarshalJSON(d []byte) (err error) {
	var a [2]json.RawMessage
	err = json.Unmarshal(d, &a)
	if err != nil {
		return
	}
	err = json.Unmarshal(a[0], &s.Timestamp)
	if err == nil {
		err = json.Unmarshal(a[1], &s.Value)
	}
	return
}
